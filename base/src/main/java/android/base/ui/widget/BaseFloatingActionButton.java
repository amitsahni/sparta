package android.base.ui.widget;

import android.content.Context;
import android.support.design.widget.FloatingActionButton;
import android.util.AttributeSet;

/**
 * Created by clickapps on 28/12/15.
 */
public class BaseFloatingActionButton extends FloatingActionButton {
    /**
     * Instantiates a new Base floating action button.
     *
     * @param context the context
     */
    public BaseFloatingActionButton(Context context) {
        super(context);
    }

    /**
     * Instantiates a new Base floating action button.
     *
     * @param context the context
     * @param attrs   the attrs
     */
    public BaseFloatingActionButton(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    /**
     * Instantiates a new Base floating action button.
     *
     * @param context      the context
     * @param attrs        the attrs
     * @param defStyleAttr the def style attr
     */
    public BaseFloatingActionButton(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }
}
